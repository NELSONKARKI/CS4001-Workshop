class LiteralPractice {
    public static void main(String[] args) {

        long a = 12345678L; 
        float b = 152.636f;
        char c = '\u0056';  // Unicode for 'V'

        System.out.println("long: " + a);
        System.out.println("float: " + b);
        System.out.println("char: " + c);
    }
}
